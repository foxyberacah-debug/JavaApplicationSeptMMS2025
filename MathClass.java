public class MathClass{
	public static void main(String[] args){
	    System.out.printf("The Absolute is %d.%n",Math.abs(-90));
        System.out.printf("The highest is %d.%n",Math.max(80,90));
        System.out.printf("The lowest is %d.%n",Math.min(69,2));
        System.out.printf("8 raised to the power of 2 equals %d.%n",Math.pow(8,2));
        System.out.printf("The generated number is %d.%n",Math.random());		
	}
}
